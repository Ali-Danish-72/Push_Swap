#include "../../libft.h"

void	*ft_memcpy(void *dst, void const *src, size_t n)
{
	void	*destination;

	if (!dst || !src)
		return (dst);
	destination = dst;
	while (n--)
		*(char *)dst++ = *(char *)src++;
	return (destination);
}
